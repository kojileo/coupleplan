/**
 * 都道府県リストを取得
 */
export function getPrefectures(): string[] {
  return locationData.prefectures;
}

/**
 * 指定した都道府県の市区町村リストを取得
 */
export function getCities(prefecture: string): string[] {
  return locationData.cities[prefecture] || [];
}

/**
 * 都道府県が存在するか確認
 */
export function isPrefectureValid(prefecture: string): boolean {
  return locationData.prefectures.includes(prefecture);
}

/**
 * 市区町村が存在するか確認
 */
export function isCityValid(prefecture: string, city: string): boolean {
  const cities = locationData.cities[prefecture];
  return cities ? cities.includes(city) : false;
}
